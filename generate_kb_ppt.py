from pptx import Presentation
from pptx.util import Inches, Pt
from pptx.enum.text import PP_ALIGN
import os

def create_kb_driven_ppt(output_path, template_path, img_dir):
    # Load template or create new
    if os.path.exists(template_path):
        prs = Presentation(template_path)
        for _ in range(len(prs.slides)):
            rId = prs.slides._sldIdLst[0].rId
            prs.part.drop_rel(rId)
            del prs.slides._sldIdLst[0]
    else:
        prs = Presentation()

    def add_slide(title, paragraphs, img=None, img_scale=7.5):
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = title
        
        # Header
        header_text = "十一五规划教材                                          《信息系统项目管理》讲义"
        txBox = slide.shapes.add_textbox(Inches(0.5), Inches(0.2), Inches(9), Inches(0.5))
        tf = txBox.text_frame
        p = tf.paragraphs[0]
        p.text = header_text
        p.font.size = Pt(12)
        slide.shapes.add_connector(1, Inches(0.5), Inches(0.7), Inches(9.5), Inches(0.7))

        body_shape = slide.placeholders[1]
        tf = body_shape.text_frame
        tf.word_wrap = True
        
        for i, text in enumerate(paragraphs):
            if i == 0:
                p = tf.paragraphs[0]
            else:
                p = tf.add_paragraph()
            p.text = text
            if text.startswith("  ") or text.startswith("- "):
                p.level = 1
                p.text = text.lstrip(" -")
            else:
                p.level = 0
            p.font.size = Pt(18)

        if img:
            img_path = os.path.join(img_dir, img)
            if os.path.exists(img_path):
                # Place image appropriately
                top_margin = Inches(4.0) if len(paragraphs) > 3 else Inches(2.0)
                slide.shapes.add_picture(img_path, Inches(1), top_margin, width=Inches(img_scale))
            else:
                # Add placeholder
                top_margin = Inches(4.0) if len(paragraphs) > 3 else Inches(2.0)
                tx_ph = slide.shapes.add_textbox(Inches(1), top_margin, Inches(8), Inches(1))
                p_ph = tx_ph.text_frame.paragraphs[0]
                p_ph.text = f"[ 图片/表格预留位: {img} ]"
                p_ph.font.color.rgb = pptx.dml.color.RGBColor(255, 0, 0)

    # Title Slide
    slide = prs.slides.add_slide(prs.slide_layouts[6])
    tx = slide.shapes.add_textbox(Inches(1), Inches(3), Inches(8), Inches(2))
    tf = tx.text_frame
    p = tf.paragraphs[0]
    p.text = "信息系统项目管理师教程（第4版）\n第11章 项目成本管理"
    p.font.bold = True
    p.font.size = Pt(44)
